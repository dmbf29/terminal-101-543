# pwd
# => present working directory (where am I?)

# ls / ll
# => short list / long list

# cd folder_name
# => changes directory

# cd .. / ..
# => moves back out of folder

# mkdir new_folder_name
# => creates a new directory

# touch filename
# => creates a new file

# rm filename
# => removes file

# rm -r folder_name
# => removes folder
# -f (to force remove)

# mv folder/file folder_name
# => move the folder or file

# mv older/file new_name
# => rename the file or folder

# stt / open .
# => opens our text editor

# open .
# => open with default program

# cat filename
# => prints file into terminal

# curl url
# => http request
